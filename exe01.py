#Peça ao usuário dois números se o primeiro número for maior que o segundo exiba primeiro o segundo número, caso contrario primeiro o primeiro numero em seguida o segundo
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))
if num1 > num2:
    print(num2)
    print(num1)
    print("Mateus De Campos Turkoco")
else:
    print(num1)
    print(num2)
    print("Mateus De Campos Turkoco")
